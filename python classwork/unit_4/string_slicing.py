'''Syntax :
string_name[starting_index:finishing_index:character_iterate]'''


str = "premprakashsingh"
print("character from 3 to 7",str[3:8])
print("character from 0 to 7",str[:8])
print("character from 8 onward",str[8:])
print("character from 0 onward",str[:])
print("character from 0 onward expcept last two ",str[:-2])
print("character from last two onward",str[-2:])
print("character from -4 to -1",str[-4:-1])
print("character from 0 to 9",str[0:10:2])




