#Range of Indexes :
'''You can specify a range of indexes by specifying where to start and
where to end the range. When specifying a range, the return value
will be a new list with the specified items :'''


mylist=["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(mylist[2:5])
print(mylist[-4:-1])      # negative range index